// Easily conversion of one base into other using tostring()

let x=10;

// -> conversion of base 10 to base 2
console.log(x.toString(2));

// -> conversion of base 10 to base 8
console.log(x.toString(8));

// -> conversion of base 10 to base 6
console.log(x.toString(6));

// -> conversion of base 10 to base 16
console.log(x.toString(16));

// -> The output is in the form of string