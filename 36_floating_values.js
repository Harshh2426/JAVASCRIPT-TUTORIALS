// toFixed.
// it count digit after point.

let a = 2.348;
console.log(a.toFixed(2));



// toPrecision.
// it count total digit.

console.log(a.toPrecision(2));



// toExponential
// it count the digit after point and round(result is in the form of e).

console.log(a.toExponential(2));



// toString
// it convert it into string

console.log(a.toString());